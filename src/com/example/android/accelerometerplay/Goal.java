package com.example.android.accelerometerplay;

public class Goal {
	private float mPosX;
    private float mPosY;
    private float radius;
    
    public Goal(final float mPosX, final float mPosY, final float radius) {
		this.mPosX = mPosX;
		this.mPosY = mPosY;
		this.radius = radius;
	}
    
    
}
